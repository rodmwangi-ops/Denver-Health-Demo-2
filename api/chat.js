function normalize(text) {
  return (text || "").toString().toLowerCase();
}

function tokenize(query) {
  const q = normalize(query);
  // Split on non-alphanumerics; keep words length >= 3
  return q.split(/[^a-z0-9]+/g).filter((w) => w && w.length >= 3);
}

function searchKnowledge(query, maxResults = 5) {
  const qLower = normalize(query);
  const words = tokenize(query);
  if (!qLower.trim() || words.length === 0) return [];

  const scored = KNOWLEDGE_BASE.chunks.map((chunk) => {
    const title = normalize(chunk.title);
    const content = normalize(chunk.content);
    const tags = (chunk.tags || []).map((t) => normalize(t));

    let score = 0;

    // +3 if the query contains a tag exactly (full tag string)
    for (const tag of tags) {
      if (!tag) continue;
      // Tag can be multi-word; treat as substring match against the full query
      if (qLower.includes(tag)) score += 3;
    }

    for (const w of words) {
      for (const tag of tags) {
        if (tag && tag.includes(w)) score += 2; // +2 if tag contains query word
      }
      if (title.includes(w)) score += 2; // +2 if title contains query word
      if (content.includes(w)) score += 1; // +1 if content contains query word
    }

    return { chunk, score };
  });

  return scored
    .filter((x) => x.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, maxResults)
    .map((x) => x.chunk);
}

function buildContext(query, maxResults = 5) {
  const matches = searchKnowledge(query, maxResults);
  if (!matches.length) return "";

  return matches
    .map((c) => {
      return [
        `CHUNK_ID: ${c.id}`,
        `SOURCE: ${c.source}`,
        `TITLE: ${c.title}`,
        `TYPE: ${c.type}`,
        `TAGS: ${(c.tags || []).join(", ")}`,
        "",
        c.content,
        "---",
      ].join("\n");
    })
    .join("\n");
}



