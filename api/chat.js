function safeJsonParse(value) {
  if (value == null) return null;
  if (typeof value === "object") return value;
  if (typeof value !== "string") return null;
  try {
    return JSON.parse(value);
  } catch {
    return null;
  }
}

function toGeminiRole(role) {
  return role === "assistant" ? "model" : "user";
}

function extractLatestUserMessage(messages) {
  if (!Array.isArray(messages)) return "";
  for (let i = messages.length - 1; i >= 0; i--) {
    const m = messages[i];
    if (m && m.role === "user" && typeof m.content === "string" && m.content.trim()) {
      return m.content.trim();
    }
  }
  return "";
}

function buildGeminiContents(systemPrompt, contextString, messages) {
  const header = [
    systemPrompt.trim(),
    "",
    "CONTEXT (Denver Health knowledge base excerpts):",
    contextString.trim(),
    "",
    "INSTRUCTIONS:",
    "- Use ONLY the context above.",
    `- If context is not relevant or missing, respond exactly: "${FALLBACK_TEXT}"`,
  ].join("\n");

  const contents = [{ role: "user", parts: [{ text: header }] }];

  // Append conversation history
  for (const m of Array.isArray(messages) ? messages : []) {
    if (!m || typeof m.content !== "string") continue;
    const text = m.content.trim();
    if (!text) continue;
    contents.push({ role: toGeminiRole(m.role), parts: [{ text }] });
  }

  return contents;
}

function sendJson(res, statusCode, payload) {
  res.statusCode = statusCode;
  res.setHeader("Content-Type", "application/json");
  res.end(JSON.stringify(payload));
}

module.exports = async function handler(req, res) {
  try {
    if (req.method !== "POST") {
      return sendJson(res, 405, { error: "Method Not Allowed" });
    }

    const apiKey = process.env.GOOGLE_API_KEY;
    if (!apiKey) {
      return sendJson(res, 500, { error: "Missing GOOGLE_API_KEY environment variable" });
    }

    const body = safeJsonParse(req.body) ?? req.body;
    const messages = body && Array.isArray(body.messages) ? body.messages : null;
    if (!messages) {
      return sendJson(res, 400, { error: "Invalid request body. Expected { messages: [...] }" });
    }

    const latestUser = extractLatestUserMessage(messages);
    if (!latestUser) {
      return sendJson(res, 400, { error: "No user message found in messages array" });
    }

    // Retrieve relevant Denver Health context
    const context = (buildContext(latestUser) || "").trim();

    // Hard guarantee your rule: if no context, do NOT call Gemini
    if (!context) {
      return sendJson(res, 200, { content: [{ text: FALLBACK_TEXT }] });
    }

    const geminiContents = buildGeminiContents(SYSTEM_PROMPT, context, messages);

    const GEMINI_MODEL = "gemini-flash-latest";
    
    const url =
  `https://generativelanguage.googleapis.com/v1beta/models/${GEMINI_MODEL}:generateContent` +
  `?key=${encodeURIComponent(apiKey)}`;

    const geminiPayload = {
      contents: geminiContents,
      generationConfig: { temperature: 0.7, maxOutputTokens: 2048 },
    };

    const geminiResp = await fetch(url, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(geminiPayload),
    });

    const raw = await geminiResp.text();
    let data = null;
    try {
      data = JSON.parse(raw);
    } catch {
      data = null;
    }

    if (!geminiResp.ok) {
      return sendJson(res, 502, {
        error: "Gemini API error",
        status: geminiResp.status,
        details: data || raw,
      });
    }

    const parts =
      data &&
      data.candidates &&
      data.candidates[0] &&
      data.candidates[0].content &&
      Array.isArray(data.candidates[0].content.parts)
        ? data.candidates[0].content.parts
        : [];

    const modelText = parts
      .map((p) => (p && typeof p.text === "string" ? p.text : ""))
      .join("")
      .trim();

    return sendJson(res, 200, { content: [{ text: modelText || FALLBACK_TEXT }] });
  } catch (err) {
    return sendJson(res, 500, {
      error: "Server error",
      message: err && err.message ? err.message : String(err),
    });
  }
};


